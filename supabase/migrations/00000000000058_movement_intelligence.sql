-- ============================================================
-- movement_sessions
-- ============================================================
create table movement_sessions (
  id uuid primary key default gen_random_uuid(),
  member_id uuid not null references auth.users(id) on delete cascade,

  timezone text not null,
  local_date date not null,

  -- Lifecycle: generated and ready to view, started once the member opens
  -- the session experience, completed, or explicitly skipped (a real,
  -- honest state — never silently dropped).
  status text not null default 'ready' check (status in (
    'ready', 'in_progress', 'completed', 'skipped'
  )),

  focus_summary text not null,
  recovery_status text not null default 'unknown' check (recovery_status in (
    'ready', 'moderate', 'limited', 'rest', 'unknown'
  )),
  estimated_duration_minutes int not null default 0,

  -- Array of {label, domain, detail} — every entry traceable to a real
  -- fact the decision engine actually read (lib/movement/rules/facts.ts),
  -- never a canned line. Same "structured jsonb bag with a documented
  -- shape" convention as evidence_refs elsewhere in this schema.
  selection_reasons jsonb not null default '[]'::jsonb,

  -- Placeholder score pending richer signals — null until there's enough
  -- completed-session history to compute one (lib/movement/score.ts),
  -- never guessed at.
  movement_score numeric,

  generated_at timestamptz not null default now(),
  started_at timestamptz,
  completed_at timestamptz,
  skipped_at timestamptz,
  skip_reason text,

  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create index movement_sessions_member_idx on movement_sessions (member_id, local_date desc);

alter table movement_sessions enable row level security;

create policy member_read_own_movement_sessions on movement_sessions
  for select
  using (member_id = auth.uid());

create policy coach_read_assigned_movement_sessions on movement_sessions
  for select
  using (
    public.has_active_role(auth.uid(), 'coach')
    and public.is_active_coach_for(auth.uid(), member_id)
  );

create policy member_insert_own_movement_sessions on movement_sessions
  for insert
  with check (member_id = auth.uid());

create policy member_update_own_movement_sessions on movement_sessions
  for update
  using (member_id = auth.uid());

create policy coach_update_assigned_movement_sessions on movement_sessions
  for update
  using (
    public.has_active_role(auth.uid(), 'coach')
    and public.is_active_coach_for(auth.uid(), member_id)
  );

create policy platform_admin_all_movement_sessions on movement_sessions
  for all
  using (public.has_active_role(auth.uid(), 'platform_administrator'));

-- ============================================================
-- movement_session_exercises
-- ============================================================
create table movement_session_exercises (
  id uuid primary key default gen_random_uuid(),
  session_id uuid not null references movement_sessions(id) on delete cascade,
  member_id uuid not null references auth.users(id) on delete cascade,

  -- Not a foreign key on purpose — see this file's header. A text key into
  -- whichever provider generated this session.
  exercise_id text not null,

  section text not null check (section in (
    'preparation', 'breathing', 'mobility', 'activation',
    'strength', 'conditioning', 'recovery'
  )),
  sequence_index int not null default 0,

  prescribed_sets int,
  prescribed_reps text,
  prescribed_tempo text,
  prescribed_rest_seconds int,
  estimated_duration_seconds int not null default 0,

  -- Why this exercise was picked over a sibling candidate (e.g. a pain
  -- report excluded the section's usual default) — null when it was
  -- simply the default pick for this section.
  substitution_reason text,

  completed boolean not null default false,
  completed_at timestamptz,
  member_notes text,

  created_at timestamptz not null default now()
);

create index movement_session_exercises_session_idx
  on movement_session_exercises (session_id, sequence_index);
create index movement_session_exercises_member_idx on movement_session_exercises (member_id);

alter table movement_session_exercises enable row level security;

create policy member_read_own_movement_session_exercises on movement_session_exercises
