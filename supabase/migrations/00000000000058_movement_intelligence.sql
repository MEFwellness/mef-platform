create policy platform_admin_all_movement_session_exercises on movement_session_exercises
  for all
  using (public.has_active_role(auth.uid(), 'platform_administrator'));

-- ============================================================================
-- Universal Registry adapter seam (migration 40) — extend the existing
-- source_feature check constraint additively, and grant a narrow member
-- insert/update policy scoped to domain='movement', mirroring exactly how
-- migration 55 opened the same seam for food_lens_pattern_comparison. A
-- completed movement session's adherence summary has no coach-review gate,
-- so — like Food Lens — the member's own session is the writer.
-- ============================================================================
alter table registry_entries drop constraint registry_entries_source_feature_check;
alter table registry_entries add constraint registry_entries_source_feature_check
  check (source_feature in (
    'body_assessment_finding', 'assessment_ai_observation', 'wearable_daily_metric',
    'food_lens_pattern_comparison', 'movement_session_completed'
  ));

create policy member_insert_own_movement_registry_entries on registry_entries
  for insert
  with check (
    member_id = auth.uid()
    and domain = 'movement'
    and source_feature = 'movement_session_completed'
  );

create policy member_update_own_movement_registry_entries on registry_entries
  for update
  using (member_id = auth.uid() and domain = 'movement')
  with check (member_id = auth.uid() and domain = 'movement');

