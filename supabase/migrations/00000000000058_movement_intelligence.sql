-- Movement Intelligence — The Rooted Reset.
--
-- IMPORTANT: this migration does NOT add a workout library or any real
-- exercise content. It adds the data architecture a daily, intelligently
-- composed movement session needs so that any exercise source (an
-- internal placeholder catalog today; Exercise.com, Physitrack, custom
-- video, or a future proprietary API tomorrow) can plug in later with zero
-- schema change — see apps/consumer-web-app/lib/movement/providers/ for
-- the provider abstraction this schema backs, mirroring exactly how
-- migration 37 (body_assessments) backs lib/body-assessment/providers/.
--
--   movement_sessions           one row per day a member is offered a
--                                movement session. Generated by the
--                                deterministic decision engine
--                                (lib/movement/rules/) from real check-in,
--                                assessment, registry, and session-history
--                                facts — never fabricated. Never versioned
--                                in place; a regenerated session for the
--                                same day is simply a new row, same
--                                "append-only history" posture as
--                                daily_checkins and body_assessments.
--
--   movement_session_exercises  one row per exercise slot placed into a
--                                session by the decision engine, tagged
--                                with the section it belongs to
--                                (preparation/breathing/mobility/
--                                activation/strength/conditioning/
--                                recovery). `exercise_id` is a plain text
--                                key into whichever provider generated the
--                                session (lib/movement/exercises/catalog.ts
--                                today) — intentionally NOT a foreign key,
--                                since a future external provider's
--                                exercise ids don't live in this database
--                                at all.
--
-- Nothing in this migration computes a "real" Movement Score beyond a
-- simple, honest adherence calculation over completed sessions — see
-- lib/movement/score.ts. It is explicitly labeled a placeholder in the UI
-- pending richer signals (wearable recovery, range-of-motion tracking).
--
-- RLS follows the exact established pattern (migration 15 helpers,
-- migrations 27-37 precedent): member_read_own / member_insert_own /
-- member_update_own, coach_read_assigned / coach_update_assigned via
-- is_active_coach_for, platform_admin_all.

